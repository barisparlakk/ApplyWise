export function resolveLocale(acceptLanguage: string | null | undefined): Locale {
  if (!acceptLanguage) return "en";

  const preferredLanguage = acceptLanguage
    .split(",")
    .map((entry, index) => {
      const [tag, ...parameters] = entry.trim().split(";");
      const qualityParameter = parameters.find((parameter) => parameter.trim().startsWith("q="));
      const parsedQuality = qualityParameter ? Number(qualityParameter.trim().slice(2)) : 1;
      const quality = Number.isFinite(parsedQuality) && parsedQuality >= 0 && parsedQuality <= 1
        ? parsedQuality
        : 0;

      return {
        index,
        quality,
        tag: tag.trim().toLowerCase(),
      };
    })
    .filter(({ quality, tag }) => quality > 0 && Boolean(tag) && tag !== "*")
    .sort((left, right) => right.quality - left.quality || left.index - right.index)[0]?.tag;

  return preferredLanguage === "tr" || preferredLanguage?.startsWith("tr-") ? "tr" : "en";
}

export function localeTag(locale: Locale): "en-US" | "tr-TR" {
  return locale === "tr" ? "tr-TR" : "en-US";
}

export function translate(locale: Locale, message: string, values?: TranslationValues): string {
  const template = locale === "tr" ? (turkishMessages[message] ?? message) : message;

  if (!values) return template;

  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}

export function createTranslator(locale: Locale): Translator {
  return (message: string, values?: TranslationValues) => translate(locale, message, values);
}
